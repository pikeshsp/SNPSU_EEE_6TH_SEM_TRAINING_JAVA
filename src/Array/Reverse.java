package Array;

import java.util.Arrays;

public class Reverse {
    static void main(String[] args) {
        int[] arr = {10, 20, 30, 40, 50};

        System.out.print("Reverse the array : ");

        for (int i = 4 ; i < arr.length ; i--) {
            System.out.print(arr[i] + " ");
        }
    }
}



